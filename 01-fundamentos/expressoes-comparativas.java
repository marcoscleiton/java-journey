public class ExpressoesComparativas {

    public static void main(String[] args) {

        int pao = 10;
        int acucar = 20;
        String titulo = "=== EXPRESSÃ•ES COMPARATIVAS ===";

        System.out.println(titulo);

        System.out.println("pao == acucar: " + (pao == acucar));
        System.out.println("pao != acucar: " + (pao != acucar));
        System.out.println("pao > acucar: " + (pao > acucar));
        System.out.println("pao < acucar: " + (pao < acucar));
        System.out.println("pao >= acucar: " + (pao >= acucar));
        System.out.println("pao <= acucar: " + (pao <= acucar));
    }
}
